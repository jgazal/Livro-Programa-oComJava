package Capitulo1;

class Example3 {

    public static void main(String[] args) {
        int w;
        double x; //declara uma variável de ponto flutuante

        w = 10;     //atribui 10 a w
        x = 10.0;   //atribui 10,0 a x

        System.out.println("Valor de w: " + w);
        System.out.println("Valor de x: " + x);
        System.out.println();   //Exibe uma linha em branco

        //Divide as duas por 4
        w = w/4;
        x = x/4;

        System.out.println("w após divisão: " + w);
        System.out.println("x após divisão: " + x);
    }
}
