package Capitulo1;

class GalToLit {

    public static void main(String[] args) {
        double gallons;
        double liters;

        gallons = 10;
        liters = gallons * 3.7854;  //converte para litros. 1 galão = 3,7854 litros

        System.out.println(gallons + " galões são " + liters + " litros");
    }
}
