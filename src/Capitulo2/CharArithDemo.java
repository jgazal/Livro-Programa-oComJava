package Capitulo2;

class CharArithDemo {

    public static void main(String[] args) {
        char ch;

        ch = 'X';
        System.out.println("ch contém " + ch);;

        ch++;   //char pode ser incrementado
        System.out.println("ch agora é " + ch);

        ch = 90;    //dá a ch o valor Z (90 em ASCII e Unicode = Z)
        System.out.println("ch agora é " + ch);

    }
}
